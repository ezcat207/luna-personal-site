// ── Helpers ───────────────────────────────────────────────────────────────────

export function getDeepModule(num: number): DeepModule | undefined {
  return SEO_DEEP_MODULES.find((m) => m.num === num);
}

export function getDeepChapter(
  moduleNum: number,
  chapterNum: number
): DeepChapter | undefined {
  return getDeepModule(moduleNum)?.chapters.find((c) => c.num === chapterNum);
}

/** Returns [prevChapter, nextChapter] or undefined at boundaries */
export function getDeepAdjacentChapters(
  moduleNum: number,
  chapterNum: number
): [DeepChapter | undefined, DeepChapter | undefined] {
  const allChapters = SEO_DEEP_MODULES.flatMap((m) => m.chapters);
  const idx = allChapters.findIndex(
    (c) => c.moduleNum === moduleNum && c.num === chapterNum
  );
  return [allChapters[idx - 1], allChapters[idx + 1]];
}
